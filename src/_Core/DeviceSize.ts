export enum DeviceSize {
    /**
     * Extra small screens (less than 576px).
     */
    XSmall,
    /**
     * Small screens (from 576px to 767px).
     */
    Small,
    /**
     * Medium screens (from 768px to 992px).
     */
    Medium,
    /**
     * Large screens (from 992px to 1199px).
     */
    Large,
    /**
     * Extra large screens (1200px or wider).
     */
    XLarge
}